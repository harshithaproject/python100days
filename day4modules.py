# This is a single line comment
""" This is a multi-line comment
that spans multiple lines
"""

print("Hello\nWorld") # \n is a newline character
print("Hello\tWorld") # \t is a tab character
print("Hello\\World") # \\ is a backslash character
print("Hello\"World\"") # \" is a double quote character
print('Hello\'World\'') # \' is a single quote character
